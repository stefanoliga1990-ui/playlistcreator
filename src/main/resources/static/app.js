function showPlaylistCreated(container, playlistName, playlistUrl, excludedTracks) {
	removeExcludedTracksMessage(container);
	container.classList.remove("error");
	container.classList.add("success");
	container.classList.remove("hidden");
	container.replaceChildren(
			document.createTextNode(`Playlist ${playlistName || "Spotify"} creata correttamente`));
	if (playlistUrl) {
		const link = document.createElement("a");
		link.className = "playlist-link";
		link.href = playlistUrl;
		link.target = "_blank";
		link.rel = "noreferrer";
		link.textContent = playlistUrl;
		container.appendChild(link);
	}
	const excluded = [...new Set(Array.isArray(excludedTracks) ? excludedTracks.filter(Boolean) : [])];
	if (excluded.length) {
		const warning = document.createElement("section");
		warning.className = "message playlist-excluded-warning";
		const title = document.createElement("p");
		title.textContent = "I seguenti brani non sono stati aggiunti alla playlist:";
		const list = document.createElement("ul");
		list.replaceChildren(...excluded.map((track) => {
			const item = document.createElement("li");
			item.textContent = track;
			return item;
		}));
		const reason = document.createElement("p");
		reason.className = "excluded-tracks-reason";
		reason.textContent = "Nessuna corrispondenza affidabile trovata.";
		warning.append(title, list, reason);
		container.insertAdjacentElement("afterend", warning);
	}
	window.scrollTo({ top: 0, behavior: "smooth" });
}

function removeExcludedTracksMessage(container) {
	const warning = container.parentElement
		? container.parentElement.querySelector(".playlist-excluded-warning")
		: null;
	if (warning) {
		warning.remove();
	}
}

async function parseJsonResponse(response) {
	const payload = await response.json().catch(() => ({}));
	if (!response.ok) {
		const error = new Error(payload.message || `Errore HTTP ${response.status}`);
		error.type = payload.type || "error";
		throw error;
	}
	return payload;
}

