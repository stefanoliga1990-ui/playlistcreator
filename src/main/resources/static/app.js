async function createSetlistPlaylist() {
	if (!state.preview) {
		showMessage("Cerca prima un artista.", true);
		closeCreateDialog();
		return;
	}
	const selectedTracks = getSelectedSetlistTracks();
	if (!selectedTracks.length) {
		showMessage("Seleziona almeno un brano.", true);
		closeCreateDialog();
		return;
	}
	setBusy(els.createForm, true);
	showLoading("Creazione playlist", `Creazione di "${els.playlistName.value.trim() || "playlist"}" in corso...`);
	try {
		const response = await fetch("/api/playlists/generate", {
			method: "POST",
			headers: { "Content-Type": "application/json" },
			body: JSON.stringify({
				artistName: state.preview.artist.name,
				selectedTracks,
				playlistName: els.playlistName.value.trim(),
				playlistDescription: els.playlistDescription.value.trim(),
			}),
		});
		const payload = await parseJsonResponse(response);
		closeCreateDialog();
		resetGenreSection();
		resetDiscographySection();
		resetTopTracksSection();
		resetSimilarArtistsSection();
		resetDiscoverSection();
		showPlaylistCreated(
			els.message,
			payload.playlist.name || els.playlistName.value.trim(),
			payload.playlist.spotifyUrl,
			payload.excludedTracks);
	}
	catch (error) {
		showMessage(error.message || "Creazione playlist non riuscita.", true);
	}
	finally {
		hideLoading();
		setBusy(els.createForm, false);
	}
}

